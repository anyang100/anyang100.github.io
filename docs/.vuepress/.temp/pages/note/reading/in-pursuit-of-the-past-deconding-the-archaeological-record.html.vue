<p>Archaeologists, in general, have not recognised the need for middle-range theory of their own. They have instead adopted the general theoretical arguments advanced by historians and ethnographers and accommodated observations from the archaeological record the them...... We need to concentrate on the development of middle-range theory - an area where ethnographic and historical observations are crucial in testing - and to use the methods for inference developed in this way to get answer to questions such as 'What does it mean?' and 'What was it like?'. Only if reliable answers to these questions can be obtained might work on the question 'Why did it happen?' be profitable.(194)</p>
