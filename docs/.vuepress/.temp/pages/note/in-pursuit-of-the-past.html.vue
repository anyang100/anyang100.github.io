<ol>
<li>體質人類學家提出質疑：南方古猿（Australopithecus）體重過輕，無法成為強悍的獵人。南方古猿是非洲斑點鬣狗的獵物，而不是獵人。非洲獵狗導致了遺址中的骨骼累積，而不是人類。（Zh. 20）</li>
